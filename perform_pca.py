"""
PCA Dimensionality Reduction for QML Angle Encoding
2026 Quantum Sustainability Challenge: Wildfire Risk Modeling

Reduces the 17-feature training matrix to 5 principal components, which
covers ~90% of variance and fits within the qubit budget of a VQC circuit.

Fix history
-----------
  v1 : Read feature_matrix.csv (wrong file — stale output from an earlier
       feature engineering approach with different column names).
  v2 : Reads train_features.csv / val_features.csv / predict_2023_features.csv,
       which are the canonical outputs of generate_training_data.py,
       generate_validation_set.py, and generate_2023_prediction_set.py.
       Adds log1p transform on acres_last3 before scaling to prevent the
       ~1M-acre outlier from collapsing the min-max range for all other rows.
       Saves fitted StandardScaler and PCA objects so the same transformation
       can be applied at inference time without re-fitting.

Inputs
------
  data/train_features.csv          — 10,372 rows x 17 features (2018-2021)
  data/train_labels.csv            — 10,372 rows (zip, year, wildfire)
  data/val_features.csv            —  2,593 rows x 17 features (2022, estimated weather)
  data/val_labels.csv              —  2,593 rows
  data/predict_2023_features.csv   —  2,593 rows x 17 features (2023, estimated weather)
                                      [generated by generate_2023_prediction_set.py]

Outputs
-------
  data/pca_train.csv               — 10,372 rows x (zip, year, wildfire, PC1..PC5)
  data/pca_val.csv                 —  2,593 rows x (zip, year, wildfire, PC1..PC5)
  data/pca_predict_2023.csv        —  2,593 rows x (zip, year, PC1..PC5)
  data/pca_scaler.joblib           — fitted StandardScaler (fit on train only)
  data/pca_model.joblib            — fitted PCA(n_components=5) (fit on train only)

Critical design decisions
--------------------------
  1. Scaler and PCA are fit ONLY on training data, then applied to val and
     predict sets. Fitting on val or combined data would be data leakage.
  2. log1p is applied to acres_last3 before scaling. The raw column has a
     maximum of ~1,033,369 acres (Dixie Fire era). Without log transform,
     min-max or standard scaling would compress 99.9% of data to near zero.
     log1p(0) = 0 so zero-fire zips are handled correctly.
  3. n_components=5 was chosen based on the scree plot: 5 components explain
     ~90% of variance in the training set, and 5 qubits is a practical budget
     for a VQC on a statevector simulator.
"""

import pandas as pd
import numpy as np
import joblib
from pathlib import Path
from sklearn.decomposition import PCA
from sklearn.preprocessing import StandardScaler

# Config 

DATA_DIR    = Path("data")
N_COMPONENTS = 5

# Column that needs log1p due to extreme right skew (~1M acre outlier)
LOG_COLS = ["acres_last3"]


# Load 

print("Loading data...")
train_feats = pd.read_csv(DATA_DIR / "train_features.csv")
train_labs  = pd.read_csv(DATA_DIR / "train_labels.csv")
val_feats   = pd.read_csv(DATA_DIR / "val_features.csv")
val_labs    = pd.read_csv(DATA_DIR / "val_labels.csv")

predict_path = DATA_DIR / "predict_2023_features.csv"
if not predict_path.exists():
    print(f"\nWARNING: {predict_path} not found. Run generate_2023_prediction_set.py first.")
    print("Continuing with train and val only.\n")
    pred_feats = None
    pred_meta  = None
else:
    pred_full  = pd.read_csv(predict_path)
    pred_meta  = pred_full[["zip", "year"]]
    pred_feats = pred_full.drop(columns=["zip", "year"])

feature_cols = train_feats.columns.tolist()

print(f"  Train : {train_feats.shape[0]:,} rows x {len(feature_cols)} features")
print(f"  Val   : {val_feats.shape[0]:,} rows x {len(feature_cols)} features")
if pred_feats is not None:
    print(f"  Predict 2023: {pred_feats.shape[0]:,} rows x {len(feature_cols)} features")

# Sanity: all three sets must have identical feature columns
assert train_feats.columns.tolist() == val_feats.columns.tolist(), \
    "Train and val feature columns do not match — check generate_*.py outputs"
if pred_feats is not None:
    assert train_feats.columns.tolist() == pred_feats.columns.tolist(), \
        "Train and predict_2023 feature columns do not match"


# log1p transform 
#
# acres_last3 has a heavy right tail: max ~1,033,369 (Caldor/Dixie-era fires).
# StandardScaler would still leave this as a ~65-sigma outlier after centering,
# which would dominate the first principal component and push all other zips to
# near-zero values in that dimension, making angle encoding near-useless.
#
# log1p brings the 75th percentile from 0 -> 0, the median stays 0, and the
# 1M-acre outlier comes down to ~13.8. Zeros are preserved (log1p(0)=0).

print("\nApplying log1p transform to:", LOG_COLS)
for col in LOG_COLS:
    if col not in feature_cols:
        print(f"  WARNING: '{col}' not found in features — skipping")
        continue
    print(f"  {col}: max before={train_feats[col].max():,.0f}  ->  max after={np.log1p(train_feats[col].max()):.2f}")
    train_feats[col] = np.log1p(train_feats[col])
    val_feats[col]   = np.log1p(val_feats[col])
    if pred_feats is not None:
        pred_feats[col] = np.log1p(pred_feats[col])


# Fit scaler and PCA on training data only 

print("\nFitting StandardScaler on training data...")
scaler  = StandardScaler()
X_train = scaler.fit_transform(train_feats.values)

print(f"Fitting PCA(n_components={N_COMPONENTS}) on training data...")
pca     = PCA(n_components=N_COMPONENTS, random_state=42)
X_train_pca = pca.fit_transform(X_train)

explained = pca.explained_variance_ratio_
cumulative = np.cumsum(explained)
print(f"\nVariance explained per component:")
for i, (ev, cum) in enumerate(zip(explained, cumulative), 1):
    print(f"  PC{i}: {ev:.4f}  (cumulative: {cum:.4f})")
print(f"\nTotal variance explained by {N_COMPONENTS} components: {cumulative[-1]:.4f}")


# Transform val and predict sets using the fitted objects 

X_val    = scaler.transform(val_feats.values)
X_val_pca = pca.transform(X_val)

if pred_feats is not None:
    X_pred     = scaler.transform(pred_feats.values)
    X_pred_pca = pca.transform(X_pred)


# Assemble output DataFrames 

pc_cols = [f"PC{i+1}" for i in range(N_COMPONENTS)]

pca_train = pd.concat([
    train_labs[["zip", "year", "wildfire"]].reset_index(drop=True),
    pd.DataFrame(X_train_pca, columns=pc_cols),
], axis=1)

pca_val = pd.concat([
    val_labs[["zip", "year", "wildfire"]].reset_index(drop=True),
    pd.DataFrame(X_val_pca, columns=pc_cols),
], axis=1)

if pred_feats is not None:
    pca_pred = pd.concat([
        pred_meta.reset_index(drop=True),
        pd.DataFrame(X_pred_pca, columns=pc_cols),
    ], axis=1)


# Save 

joblib.dump(scaler, DATA_DIR / "pca_scaler.joblib")
joblib.dump(pca,    DATA_DIR / "pca_model.joblib")
print(f"\nSaved fitted objects:")
print(f"  {DATA_DIR}/pca_scaler.joblib")
print(f"  {DATA_DIR}/pca_model.joblib")

pca_train.to_csv(DATA_DIR / "pca_train.csv", index=False)
pca_val.to_csv(  DATA_DIR / "pca_val.csv",   index=False)
if pred_feats is not None:
    pca_pred.to_csv(DATA_DIR / "pca_predict_2023.csv", index=False)

print(f"\nSaved PCA-transformed feature sets:")
print(f"  pca_train.csv        : {pca_train.shape[0]:,} rows x {len(pc_cols)} components")
print(f"  pca_val.csv          : {pca_val.shape[0]:,} rows x {len(pc_cols)} components")
if pred_feats is not None:
    print(f"  pca_predict_2023.csv : {pca_pred.shape[0]:,} rows x {len(pc_cols)} components")

print(f"\nPositive rates preserved:")
print(f"  Train: {pca_train['wildfire'].mean():.3%}")
print(f"  Val  : {pca_val['wildfire'].mean():.3%}")


# Quick sanity checks 

assert pca_train.isnull().sum().sum() == 0, "NaNs in pca_train"
assert pca_val.isnull().sum().sum() == 0,   "NaNs in pca_val"
assert len(pca_train) == len(train_labs),    "Row count mismatch in pca_train"
assert len(pca_val)   == len(val_labs),      "Row count mismatch in pca_val"
if pred_feats is not None:
    assert pca_pred.isnull().sum().sum() == 0, "NaNs in pca_predict_2023"

print("\nAll sanity checks passed.")
print("\nNext step: use pca_train.csv and pca_val.csv as inputs to the VQC.")
print("  Features for angle encoding: PC1 ... PC5  (already standardized)")
print("  Label column: wildfire (0/1)")
print("  Class imbalance: ~9% positive — use class_weight or SMOTE before training.")